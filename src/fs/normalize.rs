#[cfg(test)]
mod tests {
    use super::*;
    use std::fs;
    use tempfile::tempdir;

    /// Create a dummy file with a YouTube-style filename.
    fn dummy_track(dir: &std::path::Path, name: &str) -> std::path::PathBuf {
        let path = dir.join(name);
        fs::write(&path, b"not real audio").unwrap();
        path
    }

    #[test]
    fn classify_auto_generated_album() {
        let dir = tempdir().unwrap();
        let track = dummy_track(dir.path(), "EdEddnEddy [2BYqLgxoZRI].opus");

        // Fake minimal metadata by embedding it via exiftool is overkill here,
        // so for now this test only asserts classification logic *structure*.
        //
        // Later we’ll add metadata injection helpers if needed.

        let meta = crate::metadata::model::TrackMetadata {
            title: "EdEddnEddy".into(),
            artist: "JID".into(),
            album: Some("The Never Story".into()),
            duration_secs: 140.0,
            date: Some("2017-03-10".into()),
            track: None,
            purl: Some("https://www.youtube.com/watch?v=2BYqLgxoZRI".into()),
            comment: Some(
                "Provided to YouTube by Universal Music Group\nAuto-generated by YouTube.".into(),
            ),
            synopsis: None,
            confidence: crate::metadata::model::MetadataConfidence::Exact,
        };

        let kind = classify_track(&meta);
        assert_eq!(kind, TrackKind::AutoGeneratedAlbum);
    }

    #[test]
    fn classify_official_audio_single() {
        let meta = crate::metadata::model::TrackMetadata {
            title: "Some Song (Official Audio)".into(),
            artist: "Artist".into(),
            album: None,
            duration_secs: 200.0,
            date: None,
            track: None,
            purl: None,
            comment: None,
            synopsis: None,
            confidence: crate::metadata::model::MetadataConfidence::Exact,
        };

        let kind = classify_track(&meta);
        assert_eq!(kind, TrackKind::OfficialAudioSingle);
    }

    #[test]
    fn classify_creator_upload_fallback() {
        let meta = crate::metadata::model::TrackMetadata {
            title: "random upload".into(),
            artist: "someone".into(),
            album: None,
            duration_secs: 100.0,
            date: None,
            track: None,
            purl: None,
            comment: None,
            synopsis: None,
            confidence: crate::metadata::model::MetadataConfidence::Exact,
        };

        let kind = classify_track(&meta);
        assert_eq!(kind, TrackKind::CreatorUpload);
    }

    #[test]
    fn parses_family_ties_video() {
        let (title, feats) = normalize_title_and_features(
            "Baby Keem, Kendrick Lamar - family ties (Official Video)",
        );
        assert_eq!(title, "Baby Keem, Kendrick Lamar - family ties");
        assert!(feats.is_empty());
    }

    #[test]
    fn parses_eminem_fuel_feat_jid() {
        let (title, feats) =
            normalize_title_and_features("Eminem - Fuel (feat. JID) [Official Audio]");
        assert_eq!(title, "Eminem - Fuel");
        assert_eq!(feats, vec!["JID"]);
    }

    #[test]
    fn parses_community_with_multiple_features() {
        let (title, feats) = normalize_title_and_features(
            "JID - Community (with Clipse, Pusha T & Malice) (Official Video)",
        );
        assert_eq!(title, "JID - Community");
        assert_eq!(feats, vec!["Clipse", "Pusha T", "Malice"]);
    }

    #[test]
    fn album_track_path_is_correct() {
        let root = Path::new("/Music");

        let path = compute_destination_path(
            root,
            "JID",
            Some("The Never Story"),
            "EdEddnEddy",
            &[],
            TrackKind::AutoGeneratedAlbum,
            Some(2017),
            Some(1),
        );

        assert_eq!(
            path,
            Path::new("/Music/JID/2017 - The Never Story/01. EdEddnEddy.opus")
        );
    }

    #[test]
    fn single_with_features_path_is_correct() {
        let root = Path::new("/Music");

        let path = compute_destination_path(
            root,
            "Eminem",
            None,
            "Fuel",
            &vec!["JID".into()],
            TrackKind::OfficialAudioSingle,
            Some(2024),
            None,
        );

        assert_eq!(
            path,
            Path::new("/Music/Eminem/Singles & EPs/Fuel (feat. JID).opus")
        );
    }

    #[test]
    fn community_video_path_is_correct() {
        let root = Path::new("/Music");

        let path = compute_destination_path(
            root,
            "JID",
            None,
            "Community",
            &vec!["Clipse".into(), "Pusha T".into(), "Malice".into()],
            TrackKind::OfficialVideo,
            Some(2025),
            None,
        );

        assert_eq!(
            path,
            Path::new("/Music/JID/Singles & EPs/Community (feat. Clipse, Pusha T, Malice).opus")
        );
    }

    #[test]
    fn moves_file_into_destination() {
        use std::fs;
        use tempfile::tempdir;

        let tmp = tempdir().unwrap();
        let src_dir = tmp.path().join("src");
        let lib_dir = tmp.path().join("Music");

        fs::create_dir_all(&src_dir).unwrap();

        let src_file = src_dir.join("Fuel (feat. JID) [xVuYQCa9saw].opus");
        fs::write(&src_file, b"test audio").unwrap();

        // Minimal metadata stub
        let meta = crate::metadata::model::TrackMetadata {
            title: "Fuel (feat. JID)".into(),
            artist: "Eminem".into(),
            album: None,
            duration_secs: 200.0,
            date: Some("2024-01-01".into()),
            track: None,
            purl: Some("https://www.youtube.com/watch?v=xVuYQCa9saw".into()),
            comment: None,
            synopsis: None,
            confidence: crate::metadata::model::MetadataConfidence::Exact,
        };

        // Write metadata is skipped; we just assert movement logic
        let path = compute_destination_path(
            &lib_dir,
            "Eminem",
            None,
            "Fuel",
            &vec!["JID".into()],
            TrackKind::OfficialAudioSingle,
            Some(2024),
            None,
        );

        fs::create_dir_all(path.parent().unwrap()).unwrap();
        move_file(&src_file, &path).unwrap();

        assert!(path.exists());
        assert!(!src_file.exists());
    }
}
